# 1) 建立一個用來「合併 6 張圖片特徵」的 Attention 模組
#    這裡示範一個非常簡單的多頭注意力，將 6 個視圖合併成 1 個
# -------------------------------------------------
class MergeAttention(nn.Module):
    def __init__(self, embed_dim, num_heads=4):
        super().__init__()
        self.mha = nn.MultiheadAttention(embed_dim, num_heads)
        self.norm = nn.LayerNorm(embed_dim)
    
    def forward(self, x):
        # x: [B, T, D]，其中 T=6
        x_tbd = x.transpose(0, 1)    # -> [T, B, D]  (多頭注意力預設是 seq_first)
        attn_out, _ = self.mha(x_tbd, x_tbd, x_tbd)  # -> [T, B, D]

        # 殘差
        attn_out = attn_out.transpose(0, 1)          # 回到 [B, T, D]
        x = x + attn_out
        x = self.norm(x)

        # -------------------------------------------------
        # 這裡關鍵：把 [B, 6, D] -> [B, D]
        # -------------------------------------------------
        # 常見做法 1：平均池化
        x = x.mean(dim=1)  # [B, D]

        # 或做法 2：只取 "第一個 token"
        # x = x[:, 0, :]    # [B, D]

        return x  # [B, D]



